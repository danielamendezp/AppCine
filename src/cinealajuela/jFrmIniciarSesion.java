    private void jBtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAceptarActionPerformed
        String contrasenna=jContrasenna.getText();
        Administrar miAdmin= new Administrar(contrasenna);
        
        if(miAdmin.validarContrasenna()==true){
            jFrmAdministrar administrar=new jFrmAdministrar();
            administrar.setVisible(true);
            administrar.setLocationRelativeTo(null);
            this.setVisible(false);
            //dispose();
        }
        else{
            if(oportunidades>=3){
                jFrmCarteleras ventana= new jFrmCarteleras();
                ventana.setVisible(true);
                ventana.setLocationRelativeTo(null);
                this.setVisible(false);
                //dispose();
            }
            else{
                oportunidades++;
                JOptionPane.showMessageDialog(null, "Contrase침a Incorrecta", "", JOptionPane.OK_OPTION);
            }
        }
       
    }//GEN-LAST:event_jBtnAceptarActionPerformed

